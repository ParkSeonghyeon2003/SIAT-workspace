# 변수 선언과 값 할당
# 파이썬에서는 숫자도 객체이며 객체는 클래스로 표현됩니다.
name = "Alice"      # 문자열 <class 'str'>
age = 25            # 정수   <class 'int'>
height = 165.5      # 싨수   <class 'float'>
is_student = True   # 논리값 <class 'bool'>

print(name, type(name))             # Alice <class 'str'>
print(age, type(age))               # 25 <class 'int'>
print(height, type(height))         # 165.5 <class 'float'>
print(is_student, type(is_student)) # True <class 'bool'>