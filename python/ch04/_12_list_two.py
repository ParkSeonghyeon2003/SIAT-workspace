matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

print(matrix) # [[1, 2, 3], [4, 5, 6], [7, 8, 9]]

print(matrix[0]) # [1, 2, 3]
print(matrix[1]) # [4, 5, 6]
print(matrix[2]) # [7, 8, 9]

print(sum(matrix[0])) # 6
print(sum(matrix[1])) # 15
print(sum(matrix[2])) # 24

print(matrix[0][0]) # 1행 1열 -> 1
print(matrix[2][1]) # 3행 2열 -> 8